#include "libft.h"

int	ft_isalpha(int c)
{
	unsigned char	uc;

	uc = (unsigned char)c;
	if ((uc >= 'a' && uc <= 'z') || (uc >= 'A' && uc <= 'Z'))
		return (1);
	return (0);
}
